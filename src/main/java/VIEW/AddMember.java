    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String MemberId = txtMemberID.getText();
        String MemberName = txtName.getText();
        String Mobile =  txtMobile.getText();
        String Date =txtDate.getText();
    
 
   if(MemberId.isEmpty()||MemberName.isEmpty()||Mobile.isEmpty()||Date.isEmpty() ){
        JOptionPane.showMessageDialog(this,"all fields are required");
   }
   
    else{
         try{
        Connection conn=DatabaseConnection.getConnection();
                String sql = "Update members set Name = ?,Mobile = ?,Date = ?  Where MemberId = ?";
         PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(4,MemberId );
        pst.setString(1,MemberName );
        pst.setString(2,Mobile );  
        pst.setString(3,Date ); 
      
        
          int status = pst.executeUpdate();
        
           if (status == 1) {
            JOptionPane.showMessageDialog(rootPane, "Member  update successfully.");
        }

      
        pst.close();
       
    }
         catch(Exception ee){JOptionPane.showMessageDialog(rootPane,"error" +ee.getMessage());}
    
    }
    
    
    }//GEN-LAST:event_btnUpdateActionPerformed

